// Helper function to get category by ID
export const getCategoryById = (id: string): Category | undefined => {
  return categories.find((category) => category.id === id);
};

// Helper function to get categories by IDs
export const getCategoriesByIds = (ids: string[]): Category[] => {
  return categories.filter((category) => ids.includes(category.id));
};

// Helper function to get all words from selected categories and level
export const getWordsFromCategories = (
  categoryIds: string[],
  level: 'simple' | 'advanced',
): string[] => {
  const selectedCategories = getCategoriesByIds(categoryIds);
  const allWords: string[] = [];

  selectedCategories.forEach((category) => {
    allWords.push(...category.words[level]);
  });

  return allWords;
};
